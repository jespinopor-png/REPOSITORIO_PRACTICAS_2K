"""**Ejercicio 9**

Escribir una función que calcule el módulo de un vector.
"""

def calcular_modulo(vector):
    suma_cuadrados = 0

    for componente in vector:
        suma_cuadrados = suma_cuadrados + (componente ** 2)
    modulo = suma_cuadrados ** 0.5
    return modulo

mi_vector = [3, 4]
