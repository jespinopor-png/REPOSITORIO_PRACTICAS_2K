"""**Ejercicio 11**

Escribir una función que reciba una muestra de números y devuelva los valores atípicos, es decir, los valores cuya puntuación típica sea mayor que 3 o menor que -3. Nota: La puntuación típica de un valor se obtiene restando la media y dividiendo por la desviación típica de la muestra.
"""

import statistics
def buscar_valores_extremos(datos):
    media = statistics.mean(datos)
    desviacion = statistics.stdev(datos)
    atipicos = []

    for x in datos:
        z_score = (x - media) / desviacion
        if abs(z_score) > 1.5:
            atipicos.append(x)

    return atipicos

precios_gpu = [300, 310, 290, 305, 315, 2000]
resultado = buscar_valores_extremos(precios_gpu)

print(f"Los valores atipicos son: {resultado}")

