"""10. Escribir una función que convierta un número decimal en binario y otra que convierta un número binario en decimal."""

def decimal_a_binario(decimal):
  if decimal == 0:
    return "0"
  binario = ""
  while decimal > 0:
    binario = str(decimal % 2) + binario
    decimal //= 2
  return binario

def binario_a_decimal(binario):
  decimal = 0
  for i, digito in enumerate(reversed(binario)):
    if digito == '1':
      decimal += 2 ** i
  return decimal

num_decimal = 12
num_binario = "101"

binario_resultante = decimal_a_binario(num_decimal)
decimal_resultante = binario_a_decimal(num_binario)

print(f"El número decimal {num_decimal} en binario es: {binario_resultante}")
print(f"El número binario {num_binario} en decimal es: {decimal_resultante}")

"""11. Escribir un programa que reciba una cadena de caracteres y devuelva un diccionario con cada palabra que contiene y su frecuencia. Escribir otra función que reciba el diccionario generado con la función anterior y devuelva una tupla con la palabra más repetida y su frecuencia."""

from collections import Counter

def contar_frecuencia_palabras(cadena):
  cadena_limpia = ''.join(c.lower() if c.isalnum() or c.isspace() else ' ' for c in cadena)
  palabras = cadena_limpia.split()
  return dict(Counter(palabras))

def palabra_mas_repetida(diccionario_frecuencias):
  if not diccionario_frecuencias:
    return (None, 0)

  palabra_max = None
  frecuencia_max = 0
  for palabra, frecuencia in diccionario_frecuencias.items():
    if frecuencia > frecuencia_max:
      frecuencia_max = frecuencia
      palabra_max = palabra
  return (palabra_max, frecuencia_max)

texto = "Hola amigos :3"
frecuencias = contar_frecuencia_palabras(texto)
print(f"Frecuencia de palabras: {frecuencias}")

mas_repetida, freq = palabra_mas_repetida(frecuencias)
print(f"La palabra más repetida es '{mas_repetida}' con una frecuencia de {freq}.")