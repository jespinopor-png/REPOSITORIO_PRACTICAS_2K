"""8. Escribir una función que reciba una muestra de números en una lista y devuelva un diccionario con su media, varianza y desviación típica."""

import math

def calcular_estadisticas_muestra(lista_numeros):
  if not lista_numeros:
    return {"media": 0, "varianza": 0, "desviacion_tipica": 0}

  media = sum(lista_numeros) / len(lista_numeros)
  varianza = sum([(x - media) ** 2 for x in lista_numeros]) / len(lista_numeros)
  desviacion_tipica = math.sqrt(varianza)

  return {
      "media": media,
      "varianza": varianza,
      "desviacion_tipica": desviacion_tipica
  }

muestra = [1, 2, 3]
stats = calcular_estadisticas_muestra(muestra)
print(f"Estadísticas de la muestra {muestra}:\nMedia: {stats['media']:.2f}\nVarianza: {stats['varianza']:.2f}\nDesviación Típica: {stats['desviacion_tipica']:.2f}")

